// Helper functions

export function getStateBySlug(slug: string): State | undefined {
  return STATES.find((s) => s.slug === slug);
}

export function getCityBySlug(
  stateSlug: string,
  citySlug: string
): { state: State; city: City } | undefined {
  const state = STATES.find((s) => s.slug === stateSlug);
  if (!state) return undefined;
  const city = state.cities.find((c) => c.slug === citySlug);
  if (!city) return undefined;
  return { state, city };
}

export function getAllCities(): { state: State; city: City }[] {
  return STATES.flatMap((state) =>
    state.cities.map((city) => ({ state, city }))
  );
}

/** Top N neighborhoods per borough for prerendering. Long tail renders on-demand via ISR. */
export function getTopCitiesPerState(perState = 5): { state: State; city: City }[] {
  return STATES.flatMap((state) =>
    state.cities.slice(0, perState).map((city) => ({ state, city }))
  );
}

export const TOTAL_CITIES = STATES.reduce(
  (sum, state) => sum + state.cities.length,
  0
);
